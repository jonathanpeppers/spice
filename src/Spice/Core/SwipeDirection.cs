namespace Spice;

/// <summary>
/// Defines the direction of a swipe gesture.
/// </summary>
public enum SwipeDirection
{
	/// <summary>
	/// Swipe from right to left.
	/// </summary>
	Left,

	/// <summary>
	/// Swipe from left to right.
	/// </summary>
	Right,

	/// <summary>
	/// Swipe from bottom to top.
	/// </summary>
	Up,

	/// <summary>
	/// Swipe from top to bottom.
	/// </summary>
	Down
}
