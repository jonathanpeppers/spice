namespace Spice;

/// <summary>
/// Defines the selection mode for a CollectionView.
/// </summary>
public enum SelectionMode
{
	/// <summary>
	/// No items can be selected
	/// </summary>
	None,
	/// <summary>
	/// Only one item can be selected at a time
	/// </summary>
	Single,
	/// <summary>
	/// Multiple items can be selected
	/// </summary>
	Multiple,
}
